        public static string GetAltCode(int unicode)
        {
            if (_Cp437.ContainsKey(unicode)) return string.Format("{0}", _Cp437[unicode]);
            if (_Cp1252.ContainsKey(unicode)) return string.Format("0{0}", _Cp1252[unicode]);
            return null;
        }

        public static void DebugPrintMissing(AltCodesCollection altCodes)
        {
            var modelSet = new HashSet<int>(altCodes.AltCodes.Select(code => { int c = code.Unicode; return c; }));
            var dataSet = new HashSet<int>(_Cp437.Keys.Concat(_Cp1252.Keys).Distinct());
            dataSet.ExceptWith(modelSet);
            if (dataSet.Count > 0)
            {
                Debug.WriteLine("Unicode code-points missing from model:");
                foreach (var missingUnicode in dataSet)
                {
                    Debug.WriteLine("{0:x4}", missingUnicode);
                }
            }
        }

